# String Concatenation with +
#     We can combine (or concatenate) strings by using the + operator

first_name = 'Monty'
last_name = 'Python'

full_name = first_name + last_name

print(full_name)

# Concatenating a Space
first_name = 'Monty'
last_name = 'Python'

full_name = first_name + ' ' + last_name

full_name

